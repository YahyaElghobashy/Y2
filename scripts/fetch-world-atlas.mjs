// ============================================================
// scripts/fetch-world-atlas.mjs
//
// Generates the committed world-map data for the Travels world map so the
// PWA renders offline with NO runtime CDN dependency:
//
//   src/lib/data/world-countries-110m.json  — world-atlas TopoJSON (110m),
//                                              countries keyed by numeric id.
//   src/lib/data/iso-country-codes.ts        — numeric id -> { iso2, name, region }
//                                              + COUNTRY_NAME (by iso2) + COUNTRIES list.
//
// The TopoJSON `id` is the ISO 3166-1 numeric (M49) code; we join it to the
// `world-countries` dataset (ccn3 -> cca2/name/region) to resolve alpha-2.
//
// Re-run after a new world-atlas release:  node scripts/fetch-world-atlas.mjs
// ============================================================

import { writeFileSync, mkdirSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const DATA_DIR = resolve(__dirname, "..", "src", "lib", "data")

const TOPO_URL = "https://cdn.jsdelivr.net/npm/world-atlas@2/countries-110m.json"
const COUNTRIES_URL = "https://cdn.jsdelivr.net/npm/world-countries@5.1.0/countries.json"

const get = async (url) => {
  const r = await fetch(url)
  if (!r.ok) throw new Error(`GET ${url} -> ${r.status}`)
  return r.json()
}

const run = async () => {
  mkdirSync(DATA_DIR, { recursive: true })

  console.log("↓ TopoJSON (world-atlas 110m)…")
  const topo = await get(TOPO_URL)
  writeFileSync(
    resolve(DATA_DIR, "world-countries-110m.json"),
    JSON.stringify(topo)
  )
  console.log("  wrote world-countries-110m.json")

  console.log("↓ ISO metadata (world-countries)…")
  const countries = await get(COUNTRIES_URL)

  // numeric (ccn3) -> { iso2, name, region }
  const byNumeric = {}
  for (const c of countries) {
    if (!c.ccn3 || !c.cca2) continue
    byNumeric[c.ccn3] = {
      iso2: c.cca2,
      name: c.name?.common ?? c.cca2,
      region: c.region || "Other",
    }
  }

  // Backfill any numeric ids present in the TopoJSON but missing from the join
  // (e.g. disputed/UN-coded territories) using the TopoJSON's own name.
  const geoms = topo.objects?.countries?.geometries ?? []
  for (const g of geoms) {
    const num = String(g.id)
    if (!byNumeric[num]) {
      byNumeric[num] = {
        iso2: `X${num}`.slice(0, 2).toUpperCase(),
        name: g.properties?.name ?? `Country ${num}`,
        region: "Other",
      }
    }
  }

  const countryName = {}
  for (const meta of Object.values(byNumeric)) countryName[meta.iso2] = meta.name

  const list = Object.values(byNumeric)
    .filter((m) => !m.iso2.startsWith("X"))
    .sort((a, b) => a.name.localeCompare(b.name))

  const ts = `// AUTO-GENERATED by scripts/fetch-world-atlas.mjs — do not edit by hand.
// numeric ISO 3166-1 (TopoJSON id) -> { iso2, name, region }, plus lookups.

export type CountryMeta = { iso2: string; name: string; region: string }

export const ISO_BY_NUMERIC: Record<string, CountryMeta> = ${JSON.stringify(byNumeric, null, 2)}

/** ISO 3166-1 alpha-2 (UPPERCASE) -> common name. */
export const COUNTRY_NAME: Record<string, string> = ${JSON.stringify(countryName, null, 2)}

/** All resolvable countries, sorted by name — used for the visit autocomplete. */
export const COUNTRIES: CountryMeta[] = ${JSON.stringify(list, null, 2)}

/** Resolve a TopoJSON numeric id to ISO alpha-2 (or null). */
export function iso2FromNumeric(numericId: string | number): string | null {
  return ISO_BY_NUMERIC[String(numericId)]?.iso2 ?? null
}
`
  writeFileSync(resolve(DATA_DIR, "iso-country-codes.ts"), ts)
  console.log(
    `  wrote iso-country-codes.ts (${list.length} countries, ${Object.keys(byNumeric).length} numeric ids)`
  )
  console.log("✓ done")
}

run().catch((e) => {
  console.error("FAILED:", e.message)
  process.exit(1)
})
